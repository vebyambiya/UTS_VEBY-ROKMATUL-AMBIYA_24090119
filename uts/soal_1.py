nilaiA = int(input("masukan nilai A :"))
nilaiB = int(input("masukan nilai B :"))

hasil_perjumlahan = nilaiA + nilaiB
hasil_pengurangan = nilaiA - nilaiB
hasil_perkalian = nilaiA * nilaiB


print("hasil perjumlahan")
print(hasil_perjumlahan)
print("hasil pengurangan")
print(hasil_pengurangan)
print("hasil perkalian")
print(hasil_perkalian)